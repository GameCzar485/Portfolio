class Star:
    def __init__(self, x, y, z):
        self.x, self.y, self.z = x, y, z

    @property
    def distance(self):
        return self.x**2 + self.y**2 + self.z**2

def getIndex(type):
    return type[0]


def k_closest_stars(k, stars):

    # Initialize dummy lists
    iList = []
    dList = []

    # For loop to rip and append distances to dList
    for i in range(0, len(stars)):
        dist = Star(stars[i][0], stars[i][1], stars[i][2]).distance
        dList.append([dist, i])

    # Sort dList
    dList.sort(key=getIndex)

    # Append indexes to iList based on dList
    for j in range(0, k):
        iList.append(dList[j][1])

    # Return iList
    return iList

